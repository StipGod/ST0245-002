
public class DLC {
    int D;
    int L;
    char C;
    
    public DLC(int D,int L,char C) {
        this.D = D;
        this.L = L;
        this.C = C;
    }
    public int getD() {
        return D;
    }
    public int getL() {
        return L;
    }
    public char getC() {
        return C;
    }
    public void setD(int d) {
        D = d;
    }
    public void setL(int l) {
        L = l;
    }
    public void setC(char c) {
        C = c;
    }
}
