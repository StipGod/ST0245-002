public class LC {
    int L;
    String C;
    
    public LC(int L,String C) {
        this.L = L;
        this.C = C;
    }
    public int getL() {
        return L;
    }
    public String getC() {
        return C;
    }
    public void setL(int l) {
        L = l;
    }
    public void setC(String c) {
        C = c;
    }
}

